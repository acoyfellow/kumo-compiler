import fs from 'node:fs';
import path from 'node:path';
import {fileURLToPath} from 'node:url';
import {digest} from './index.mjs';

export const DIALOG_LAYER_VERSION = 'kumo.dialog-layer/v1';
const here = path.dirname(fileURLToPath(import.meta.url));
const CONTRACT_VERSION = 'kumo.observable/v1';
export const DIALOG_LAYER_COMPONENTS = ['dialog'];
const note = (field, reason) => ({field, status: 'unknown', reason});

// Derives the supported, observable dialog open/close lifecycle asserted by the canonical
// vectors: a compound trigger button carrying the canonical data-kumo attributes, no dialog
// in the closed tree, click-to-open portaling exactly one [role=dialog] into the portal
// container, an open:<bool> event on each transition, close via the data-kumo-part=close
// control, and focus restoration to the trigger. Deep modality (inert, scroll-lock, focus
// containment), positioning, generated ids, and alertdialog escape policy remain explicit
// unknowns — they require canonical browser execution that this contract does not assert.
export function deriveDialogLayer(contracts) {
  const byName = new Map(contracts.map(contract => [contract.component, contract]));
  const contract = byName.get('dialog');
  if (contract?.schemaVersion !== CONTRACT_VERSION) throw new Error('dialog canonical contract required');
  const ids = contract.vectors.map(v => v.id);
  for (const required of ['closed-trigger', 'open-and-close']) {
    if (!ids.includes(required)) throw new Error(`dialog canonical vector ${required} missing`);
  }
  const closed = contract.vectors.find(v => v.id === 'closed-trigger');
  const triggerAttrs = closed.expected.root.attributes.includes;
  if (triggerAttrs['data-kumo-component'] !== 'Dialog' || triggerAttrs['data-kumo-part'] !== 'trigger') {
    throw new Error('dialog canonical trigger attributes missing');
  }
  const value = {
    schemaVersion: DIALOG_LAYER_VERSION,
    component: 'dialog',
    support: 'supported',
    api: {exports: contract.publicApi.exports, compound: ['.Root', '.Trigger', '.Title', '.Description', '.Close']},
    trigger: {tag: 'button', attributes: {'data-kumo-component': 'Dialog', 'data-kumo-part': 'trigger'}},
    closed: {dialogCount: 0},
    open: {role: 'dialog', portalCount: 1, container: 'KumoPortalProvider container or document.body'},
    lifecycle: {
      openOn: 'click trigger',
      closeOn: 'click [data-kumo-part=close]',
      event: 'open:<bool>',
      events: ['open:true', 'open:false']
    },
    focus: {restore: 'trigger'},
    unknowns: [
      note('modality', 'Inert outside content, scroll-lock, and Tab focus containment require canonical browser execution not asserted by the contract.'),
      note('generatedIds', 'Title/description association ids are generated by Base UI and are not stable contract surface.'),
      note('alertdialogEscape', 'Alertdialog escape policy is not asserted by the canonical contract.')
    ],
    vectorIds: ids,
    provenance: {
      component: 'dialog',
      contractPath: 'contracts/kumo.observable/v1/components/dialog.json',
      contractDigest: digest(contract),
      vectorIds: ids
    }
  };
  return {...value, capabilityDigest: digest(value)};
}

export function validateDialogLayer(value) {
  if (value?.schemaVersion !== DIALOG_LAYER_VERSION) throw new Error('invalid dialog-layer capability');
  if (value.support !== 'supported') throw new Error('dialog-layer must prove supported behavior');
  if (value.trigger?.tag !== 'button' || value.trigger.attributes['data-kumo-component'] !== 'Dialog' || value.trigger.attributes['data-kumo-part'] !== 'trigger') throw new Error('dialog-layer trigger invalid');
  if (value.closed?.dialogCount !== 0 || value.open?.role !== 'dialog' || value.open.portalCount !== 1) throw new Error('dialog-layer open/closed model invalid');
  if (value.lifecycle?.event !== 'open:<bool>' || value.focus?.restore !== 'trigger') throw new Error('dialog-layer lifecycle/focus invalid');
  if (!Array.isArray(value.unknowns) || value.unknowns.some(x => !x.field || x.status !== 'unknown' || !x.reason)) throw new Error('dialog-layer unknowns must remain explicit');
  const {capabilityDigest, ...unsigned} = value;
  if (capabilityDigest !== digest(unsigned)) throw new Error('dialog-layer capability digest mismatch');
  return value;
}

export function loadDialogLayer(file = path.join(here, 'capabilities/dialog-layer.json')) {
  return validateDialogLayer(JSON.parse(fs.readFileSync(file, 'utf8')));
}
